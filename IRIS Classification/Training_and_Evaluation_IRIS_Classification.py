import pandas as pd
import numpy as np
from sklearn.datasets import load_iris

#load the data set
iris = load_iris(as_frame=True)

x = iris.data #data matrix

x.head()
x.info()
x.describe()

feature_names = iris.feature_names #names of dataset columns

y = iris.target #classification target
target_names = iris.target_names #names of target classes

print("Class Names: ", iris.target_names)
print("Feature Names: ", iris.feature_names)

#shape
print("Shape: ", x.shape)
print("Total Samples: ", x.shape[0])
print("Total Features: ", x.shape[1])

species_count, counts = np.unique(y, return_counts=True)

print("Total Classes: ", len(species_count))

for name, count in zip(target_names, counts):
    print(f"Sample count for {name}: {count}")

"""# Training"""

#models
from sklearn.tree import DecisionTreeClassifier
from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier
from sklearn.neighbors import KNeighborsClassifier

#preprocessing
from sklearn.pipeline import Pipeline
from sklearn.preprocessing import StandardScaler

#evaluation
from sklearn.model_selection import StratifiedKFold, cross_validate

#splitting the data
kf = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

#models
decision_tree = DecisionTreeClassifier(max_depth=3, random_state=42)
random_forest = RandomForestClassifier(max_depth=3, random_state=42)
gradient_boosting = GradientBoostingClassifier(random_state=42)
k_neighbors = KNeighborsClassifier(n_neighbors=5)

models = {
    "Decision Tree" : decision_tree,
    "Random Forest" : random_forest,
    "Gradient Boosting" : gradient_boosting,
    "K-Neighbors" : k_neighbors
}

trained_model_results = []

for name, model in models.items():
  #apply scaling
  pipeline = Pipeline([
      ('scaler', StandardScaler()),
      ('classifier', model)
  ])

  #train and score data
  result = cross_validate(pipeline, x, y, cv=kf, scoring=["accuracy", "f1_weighted"], return_train_score=True)

  #save scores
  for test_acc, train_acc, test_f1, train_f1 in zip(
        result["test_accuracy"],
        result["train_accuracy"],
        result["test_f1_weighted"],
        result["train_f1_weighted"]
    ):
        trained_model_results.append({
            "Model": name,
            "Test Error": 1 - test_acc,
            "Train Error": 1 - train_acc,
            "Accuracy": test_acc,
            "F1-Score": test_f1,
        })

"""# Evaluation"""

import seaborn as sns
import matplotlib.pyplot as plt

results = pd.DataFrame.from_dict(trained_model_results)
results

results_melted = results.melt(id_vars="Model", var_name="Metric", value_name="Score")
