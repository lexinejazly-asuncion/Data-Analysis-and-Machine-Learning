import pandas as pd
import numpy as np
from sklearn.datasets import load_iris

#load the data set
iris = load_iris(as_frame=True)

x = iris.data #data matrix

x.head()
x.info()
x.describe()

feature_names = iris.feature_names #names of dataset columns

y = iris.target #classification target
target_names = iris.target_names #names of target classes

print("Class Names: ", iris.target_names)
print("Feature Names: ", iris.feature_names)

#shape
print("Shape: ", x.shape)
print("Total Samples: ", x.shape[0])
print("Total Features: ", x.shape[1])

species_count, counts = np.unique(y, return_counts=True)

print("Total Classes: ", len(species_count))

for name, count in zip(target_names, counts):
    print(f"Sample count for {name}: {count}")

"""# Training"""

#models
from sklearn.tree import DecisionTreeClassifier
from sklearn.ensemble import RandomForestClassifier, GradientBoostingClassifier
from sklearn.neighbors import KNeighborsClassifier

#evaluation
from sklearn.model_selection import StratifiedKFold, cross_validate

kf = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

decision_tree = DecisionTreeClassifier()
random_forest = RandomForestClassifier()
gradient_boosting = GradientBoostingClassifier()
k_neighbors = KNeighborsClassifier()

models = {
    "Decision Tree" : decision_tree,
    "Random Forest" : random_forest,
    "Gradient Boosting" : gradient_boosting,
    "K-Neighbors" : k_neighbors
}

trained_model_results = []

for name, model in models.items():
  result = cross_validate(model, x, y, cv=kf, scoring=["accuracy", "f1_weighted"])

  for acc, f1 in zip(result["test_accuracy"], result["test_f1_weighted"]):
        trained_model_results.append({
            "Model": name,
            "Accuracy": acc,
            "F1-Score": f1
  })

"""# Evaluation"""

import seaborn as sns
import matplotlib.pyplot as plt

results = pd.DataFrame.from_dict(trained_model_results)
results.head()

results_melted = results.melt(id_vars="Model", var_name="Metric", value_name="Score")
results_melted.head()

sns.catplot(
    data=results_melted,
    x="Model",
    y="Score",
    hue="Metric",
    kind="point",
    errorbar="sd",
    linestyle='none',
    dodge=0.25,
    aspect=2,
    height=7,
    palette="flare"
)

plt.title("Comparison of Accuracy and F1-Score across Models");

sns.catplot(
    data=results_melted,
    x="Score",
    y="Model",
    hue="Metric",
    kind="box",
    showmeans=True,
    aspect=2,
    palette="flare"
)

plt.title("Distribution of Accuracy and F1-Score (Min, Max, and Median)");